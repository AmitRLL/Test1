 package JavaLec;

public class variable {
	public static void main(String[] args) {
		//Data type
		//1.primitive type
		//variable
		//integer up to ten digit
		int a = 10;
		int b = 15;
		int c = 22;
		System.out.println(a+b+c);
		
		//boolean use only for true and false
		boolean d = true;
		boolean e = false;
		System.out.println(d);
		System.out.println(e);
		
		//character up to one digit
		char f = '2';
		System.out.println(f);
		
		//Byte up to two digit
		byte g = 12;
		System.out.println(g);
		
		//short up to five digit
		short h = 12345;
		System.out.println(h);
		
		//long more than ten digit just use capital'L' at the end
		long i = 1234567890125547L;
		System.out.println(i);
		
		//float uer 'F'
		float j = 341.89544F;
		System.out.println(j);
		
		//double
		double k = 52.5654;
		System.out.println(k);
		
		
		//2.Non primitive type
		//String
		String l = "i am Amit";
		System.out.println(l.length());//it show the length of string
		System.out.println(l);
		
		int m = 6;
		int n = 7;
		int o = m+n;
		System.out.println(o);
		

		
		
	}
		
		



	}



