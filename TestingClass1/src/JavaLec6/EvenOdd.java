package JavaLec6;

public class EvenOdd {
	public static void main(String[] args) {
		for(int a=0;a<=5;a++) {
			System.out.print((a*2)+1);
			System.out.print(",");
			
		}
		System.out.println();
		
		
		for(int a=1;a<=5;a++) {
			System.out.print(a*2);
			System.out.print(",");
			
		}
		System.out.println();
	}

}
